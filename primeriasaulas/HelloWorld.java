package primeriasaulas;
/*
 *  HelloWorld = Nome da minha classe
 *  public = Tipo de acesso da classe
 *  class = Tipo da classe
 */
public class HelloWorld{
    /*
     *  public = Tipo de acesso do método
     *  static = Tipo do método
     *  void = Tipo de retorno do método
     *  main = Nome do método
     *  String[] args = Argumentos do método
     */
    public static void main(String[] args){
        // Imprime na tela a mensagem
        System.out.println("Hello World!");
    }
}